// Dual-Mode Data Loader
async function loadData(key) {
    try {
        const response = await fetch(`data/${key}.json`);
        if (!response.ok) {
            throw new Error(`Failed to fetch ${key} data`);
        }
        return await response.json();
    } catch (error) {
        console.warn(`Fetch for '${key}' failed. Using fallback local data. Details:`, error.message);
        return fallbackData[key];
    }
}

// Global WhatsApp redirect helper
function launchWhatsApp(phone, text) {
    const cleanPhone = phone.replace(/[^0-9]/g, '');
    const whatsappUrl = `https://api.whatsapp.com/send?phone=${cleanPhone}&text=${encodeURIComponent(text)}`;
    window.open(whatsappUrl, '_blank');
}

// Sidebar Toggle Helper for Mobile
document.addEventListener('DOMContentLoaded', () => {
    const toggleBtn = document.getElementById('sidebar-toggle');
    const sidebar = document.querySelector('.sidebar-premium');
    if (toggleBtn && sidebar) {
        toggleBtn.addEventListener('click', () => {
            sidebar.classList.toggle('show');
            const icon = toggleBtn.querySelector('i');
            if (icon) {
                icon.classList.toggle('fa-bars');
                icon.classList.toggle('fa-times');
            }
        });
        
        // Close sidebar if user clicks outside of it on mobile
        document.addEventListener('click', (e) => {
            if (window.innerWidth < 992 && !sidebar.contains(e.target) && !toggleBtn.contains(e.target)) {
                if (sidebar.classList.contains('show')) {
                    sidebar.classList.remove('show');
                    const icon = toggleBtn.querySelector('i');
                    if (icon) {
                        icon.classList.remove('fa-times');
                        icon.classList.add('fa-bars');
                    }
                }
            }
        });
    }
});

